	/**
	 * 获取时间查询段
	 * 
	 * @param starttime
	 * @param endtime
	 * @return
	 */
	public static Map<String, String> getQueryData(String starttime,
			String endtime) {
		String today = "";
		String todayAgo = "";
		String today14Ago = "";
		String temp_start = "";
		String temp_end = "";
		Map<String, String> map = new HashMap<String, String>();
		Date beginDate = new Date();
		SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");// 设置日期格式
		today = df.format(beginDate);
		Calendar date = Calendar.getInstance();
		date.setTime(beginDate);
		date.set(Calendar.DATE, date.get(Calendar.DATE) - 7);

		Calendar temp_start_date = Calendar.getInstance();
		temp_start_date.setTime(beginDate);
		temp_start_date.set(Calendar.DATE,
				temp_start_date.get(Calendar.DATE) - 14);

		try {
			Date endDate = df.parse(df.format(date.getTime()));
			Date startDate = df.parse(df.format(temp_start_date.getTime()));
			today14Ago = df.format(startDate);
			todayAgo = df.format(endDate);
		} catch (ParseException e) {
			e.printStackTrace();
		}
		/**
		 * 如果开始时间和结束时间都为空
		 * */
		if (isStringEmpty(starttime) && isStringEmpty(endtime)) {
			temp_start = todayAgo;
			temp_end = today;
		}

		/**
		 * 如果开始时间和结束时间都不为空
		 * */
		if (!isStringEmpty(starttime) && !isStringEmpty(endtime)) {
			temp_start = starttime;
			temp_end = endtime;
		}

		/**
		 * 如果开始时间不为空,而且结束时间为空,那么结束时间为今天
		 * */
		if (!isStringEmpty(starttime) && isStringEmpty(endtime)) {
			temp_start = starttime;
			temp_end = today;
		}

		/**
		 * 如果开始时间为空,而且结束时间不为空,则开始为今天
		 * */
		if (isStringEmpty(starttime) && !isStringEmpty(endtime)) {
			temp_start = today14Ago;
			temp_end = today;
		}

		map.put("starttime", temp_start);
		map.put("endtime", temp_end + " 23:59:59");

		return map;

	}

